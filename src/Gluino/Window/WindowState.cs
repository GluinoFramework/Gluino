namespace Gluino;

/// <summary>
/// Represents the state of a <see cref="Window"/>.
/// </summary>
public enum WindowState
{
    /// <summary>
    /// The window is in its normal state.
    /// </summary>
    Normal,
    /// <summary>
    /// The window is minimized.
    /// </summary>
    Minimized,
    /// <summary>
    /// The window is maximized.
    /// </summary>
    Maximized
}