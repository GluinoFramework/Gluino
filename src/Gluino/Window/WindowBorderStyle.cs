namespace Gluino;

public enum WindowBorderStyle
{
    /// <summary>
    /// A resizable window with a caption.
    /// </summary>
    Sizable,
    /// <summary>
    /// A resizable window without a caption.
    /// </summary>
    SizableNoCaption,
    /// <summary>
    /// A fixed window with a caption.
    /// </summary>
    Fixed,
    /// <summary>
    /// A fixed window without a caption.
    /// </summary>
    FixedNoCaption,
    /// <summary>
    /// A borderless window.
    /// </summary>
    None
}
