namespace Gluino;

/// <summary>
/// Represents the theme of a <see cref="Window"/>.
/// </summary>
public enum WindowTheme
{
    /// <summary>
    /// The current system theme.
    /// </summary>
    System,
    /// <summary>
    /// The light theme.
    /// </summary>
    Light,
    /// <summary>
    /// The dark theme.
    /// </summary>
    Dark
}
